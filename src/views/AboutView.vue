      <h2>Operating principles</h2>
      <p>We are local-first. Police systems are locally operated, and data needs to be in its original context to be
        useful. We create tools to help people work with local data everywhere.</p>
      <p>We are not creating a massive, interoperable database. This is partly a defense mechanism against weaponized data
        about criminalized people, and partly because our users simply aren't asking for it.</p>
      <p>There are <a href="https://airtable.com/shr43ihbyM8DDkKx4" target="_blank">tens of thousands</a> of police
        agencies in the United States. No one can be an expert on all of them.</p>
      <p>The people impacted by a local agency know best what should be archived and how it should be accessed.</p>
      <p>Currently, people share data with each other through existing networks and relationships. We are augmenting these
        relationships, not replacing them.</p>
      <p>We are only interested in solutions which could survive the collapse of our organization. Our work is
        open-source.</p>
      <p>We're using Pittsburgh, PA as our headquarters and a proving ground for our early work. As we find ways to add
        value here, we can expand the strategies to work everywhere.</p>
